    private void BrowseBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BrowseBtn1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
int result = fileChooser.showOpenDialog(this);
if (result == JFileChooser.APPROVE_OPTION) {
    File selectedFile = fileChooser.getSelectedFile();
    String ID = (String)selectedFile.getAbsolutePath();
    idfile.setText(ID);
}
    }//GEN-LAST:event_BrowseBtn1ActionPerformed

    private void submitbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbuttonActionPerformed
        if(LicenseYes.isSelected()){
            JOptionPane.showMessageDialog(null, "Your Application has been submitted");
            try {
            FileWriter writer = new FileWriter("D:\\Information.txt"); 
            writer.write(print());
            writer.close();
        } catch (IOException ex) {
            Logger.getLogger(FinancePageJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        }else{
            JOptionPane.showMessageDialog(null, "You need a Valid License");
            this.setVisible(false);
        new homePageJFrame().setVisible(true);
        }

        
    }//GEN-LAST:event_submitbuttonActionPerformed

