    public String print(){
        String info;
        info = " Personal Information." + "\n ------------------------------------------------------------" + "\n Full Name: " + 
                nametext.getText() + " " + surnametext.getText() + "\n ID Number: " + IDtext.getText() + "\n Cellphone Number: " + celltext.getText()
                + "\n Gender: " + gender() + "\n Occupation: " + occupationtext.getText() + "\n Home Address: " + addresstext.getText() + "\n Work Address: " + worktext.getText()
                + "\n Gross Pay: " + grosspaytext.getText() + "\n Net Pay: " + netpaytext.getText() + "\n Valid License: " + License() + "\n Model: " + model() + 
                "\n \n Next of Kin Information. \n ------------------------------------------------------------" + "\n Full Name: " + kinnametext.getText() + " " + kinsurnametext.getText()
                + "\n Cellphone Number: " + kincelltext.getText() + "\n RelationShip: " + kinrelationshiptext.getText();
        
        return info;
    }
    
    public String gender(){
        String text = (String)genderbox.getSelectedItem();
        if(text.contains("Male")){
            return "Male";
        }else{
            return "Female";
        }   
    }
    
    public String License(){
        if(LicenseYes.isSelected()){
            return "Yes";
        }else{
            return "No";
        }   
    }
    
    public String model(){
        String select = (String)genderbox.getSelectedItem();
        if(select.contains("911")){
            return "911";
        }else if(select.contains("Taycan")){
            return "Taycan";
        }else if(select.contains("Panamera")){
            return "Panamera";
        }else{
            return "Macan";
        }
    }
